using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Client01
{
	public struct Vector3
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Member variables

		public float x;
		public float y;
		public float z;

		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Constructors

		public Vector3(float x, float y, float z)
		{
			this.x = x;
			this.y = y;
			this.z = z;
		}

		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		// Member functions

		public override string ToString()
		{
			return String.Format("{0}, {1}, {2}", x, y, z);
		}
	}
}
